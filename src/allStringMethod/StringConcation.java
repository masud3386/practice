package allStringMethod;

public class StringConcation {

	public static void main(String[] args) {
		String s="automation";
		String s1="FrameWork";
		System.out.println(s.concat("FrameWork"));
		System.out.println(s+s1);
		System.out.println(s.concat(s1));
		System.out.println("automation".concat(s1));
		System.out.println("automation"+"FrameWork");

	}

}
